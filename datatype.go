package main

import "fmt"

func main() {
	//number
	fmt.Println(1)   //int ype
	fmt.Println(1.1) //float type

	// string
	fmt.Println("freza")
	fmt.Println("freza"[1])
	fmt.Println("freza " + "nugraha")

	// bolean
	fmt.Println(true && true)
	fmt.Println(true && false)
	fmt.Println(true || true)
	fmt.Println(true || false)
	fmt.Println(!true)

}
